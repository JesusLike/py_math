'''
Arithmetic operations on numbers
'''

def add(a, b):
    '''
    Sum of two numbers
    '''
    return a + b

def substract(a, b):
    '''
    Substraction of two numbers
    '''
    return a - b

def multiply(a, b):
    '''
    Multiplication of two numbers
    '''
    return a * b

def divide(a, b):
    '''
    Division of two numbers
    '''
    return a / b
